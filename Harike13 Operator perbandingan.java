//Operator perbandingan
class HariKe13 {
    public static void main(String[] args) {
        int a = 8;
        int b = 9;
        System.out.println(a==b);
        //jika di Run akan manghasilkan false karena kedua variabel tersebut tidak senilai 
        System.out.println(a!=b);
        //jika di Run akan menghasilkan true kedua variabel tidak senilai
        System.out.println(a>=b);
        //Apabila di Run akan menghasilkan false
        System.out.println(a<b);
        //Jika di Run akan menghasilkan true
        System.out.println(a<=b);
        //Jika di Run akan menghasilkan true
    }
}
