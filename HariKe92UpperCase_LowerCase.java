import java.util.Scanner;
import java.lang.String;
public class HariKe92UpperCase_LowerCase {
public static void main(String[] args) {
        String kalimat = "UntuK mengubah huruf kapital dan huruf kecil ";
        //Untuk to.UpperCase adalah mengubah huruf kecil menjadi huruh kapital sedang huruf kapital ke huruf kecil itu adalah toLowerCase
        
        System.out.println(kalimat.toUpperCase());
        System.out.println(kalimat.toLowerCase());
    }
 } 
