//Jika program ini di jalankan maka yang muncul adalah semua angka yang ada dalam variabel a

class HeriKe33arrayfor {
    public static void main(String[] args) {
        int [] a ={5,7,6,8,9};
        int semua = a.length;
        for(int b = 0; b < semua ; b++){
            System.out.println(a[b]);
        }
    }
}
